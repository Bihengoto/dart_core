void main(List<String> args) {
  const age = 20;

  print(age + 30);
  print(age - 10);
  print(age * 20);
  print(age / 10);
  print(age ~/ 3);
  print(age % 3);
  print(age == 20);
  print(age != 50);

  print(age > 40);
  print(age < 30);
  print(age <= 10);
  print(age >= 10);

  print(age & 20); // bitwise and
  print(age | 20); // bitwise or
  print(age ^ 20); // bitwise xor

  print(age << 20);
  print(age >> 20);
}
